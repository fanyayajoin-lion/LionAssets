def update_index() -> None:
    path = ROOT / "index.md"
    text = path.read_text(encoding="utf-8")
    block = "\n".join(
        ["## 商業模式總結", "", f"- [[wiki/synthesis/{OVERVIEW}]]"]
        + [f"- [[wiki/concepts/{item['name']}]] / [[wiki/synthesis/{item['name']}案例總結]]" for item in FRAMEWORKS]
    )
    start = text.index("## 商業模式總結")
    end = text.index("## 目前可用 Workflow")
    text = text[:start] + block + "\n\n" + text[end:]
    write(path, text)


def update_hot() -> None:
    path = ROOT / "hot.md"
    text = path.read_text(encoding="utf-8")
    addition = f"""

## 可直接使用的商業框架總結

- [[wiki/synthesis/{OVERVIEW}]]
{chr(10).join(f"- [[wiki/synthesis/{item['name']}案例總結]]" for item in FRAMEWORKS)}
"""
    marker = "## 可直接使用的商業框架總結"
    if marker in text:
        text = text[: text.index(marker)].rstrip() + addition
    else:
        text = text.rstrip() + addition
    write(path, text)


def update_log() -> None:
    path = ROOT / "log.md"
    text = path.read_text(encoding="utf-8") if path.exists() else "# Log\n"
    entry = """

## 2026-06-05 商業框架總結補齊

- 依照品牌案例資料庫，補齊 12 個 business_framework 的概念頁與案例總結頁。
- 新增 [[wiki/synthesis/商業模式框架總覽]]，作為顧問診斷與自媒體選題入口。
- 原則：不新增複雜分類，讓同一品牌可同時連到多個底層框架。
- 狀態：基於既有 PPT 摘要整理，可直接使用；最新品牌資料仍需逐案補全。
"""
    if "## 2026-06-05 商業框架總結補齊" not in text:
        text = text.rstrip() + entry
    write(path, text)


def main() -> None:
    for item in FRAMEWORKS:
        write(ROOT / "wiki" / "concepts" / f"{item['name']}.md", concept_page(item))
        write(ROOT / "wiki" / "synthesis" / f"{item['name']}案例總結.md", synthesis_page(item))
    write(ROOT / "wiki" / "synthesis" / f"{OVERVIEW}.md", overview_page())
    update_index()
    update_hot()
    update_log()


if __name__ == "__main__":
    main()
